package log

//||------------------------------------------------------------------------------------------------||
//|| Active Logger
//||------------------------------------------------------------------------------------------------||

var current Logger = DefaultLogger{Module: "app"}

//||------------------------------------------------------------------------------------------------||
//|| Init returns a DefaultLogger bound to the given module name.
//||------------------------------------------------------------------------------------------------||

func Init(module string) Logger {
	current = DefaultLogger{Module: module}
	return current
}

//||------------------------------------------------------------------------------------------------||
//|| ReplaceLogger swaps out the active logger globally.
//||------------------------------------------------------------------------------------------------||

func ReplaceLogger(l Logger) {
	if l != nil {
		current = l
	}
}
