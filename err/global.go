package err

//||------------------------------------------------------------------------------------------------||
//|| GLOBAL ERRORS
//||------------------------------------------------------------------------------------------------||

func init() {
	//||------------------------------------------------------------------------------------------------||
	//|| Add Library
	//||------------------------------------------------------------------------------------------------||
	lib := AddLibrary("GLOBAL")
	//||------------------------------------------------------------------------------------------------||
	//|| GLOBAL ERRORS
	//||------------------------------------------------------------------------------------------------||
	register := []ErrorRow{
		{"UNKNOWN", "Unknown Error", EFATAL},
		{"INVALID_REQUEST", "Invalid Request", EERROR},
	}
	//||------------------------------------------------------------------------------------------------||
	//|| GLOBAL ERRORS
	//||------------------------------------------------------------------------------------------------||
	for _, row := range register {
		lib.RegisterError(row.Code, row.Message, row.Level)
	}
}
