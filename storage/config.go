package storage

//||------------------------------------------------------------------------------------------------||
//|| Backend
//||------------------------------------------------------------------------------------------------||

type StoreBackend string

//||------------------------------------------------------------------------------------------------||
//|| Backend
//||------------------------------------------------------------------------------------------------||

const (
	StorageS3    StoreBackend = "S3"
	StorageMinIO StoreBackend = "MINIO"
	StorageAzure StoreBackend = "AZURE"
	StorageGCP   StoreBackend = "GCP"
	StorageLocal StoreBackend = "LOCAL"
)

//||------------------------------------------------------------------------------------------------||
//|| Store Config
//||------------------------------------------------------------------------------------------------||

type StoreConfig struct {
	Backend StoreBackend `json:"backend"`
	//||------------------------------------------------------------------------------------------------||
	//|| General
	//||------------------------------------------------------------------------------------------------||
	Bucket    string `json:"bucket"`
	Region    string `json:"region,omitempty"`
	Endpoint  string `json:"endpoint,omitempty"` // For MinIO/S3 custom endpoint
	AccessKey string `json:"access_key,omitempty"`
	SecretKey string `json:"secret_key,omitempty"`
	//||------------------------------------------------------------------------------------------------||
	//|| Azure Specific
	//||------------------------------------------------------------------------------------------------||
	AccountName string `json:"account_name,omitempty"`
	AccountKey  string `json:"account_key,omitempty"`
	//||------------------------------------------------------------------------------------------------||
	//|| GCP Specific
	//||------------------------------------------------------------------------------------------------||
	CredentialsJSON string `json:"credentials_json,omitempty"`
	//||------------------------------------------------------------------------------------------------||
	//|| Local Storage Specific
	//||------------------------------------------------------------------------------------------------||
	LocalPath string `json:"local_path,omitempty"`
	//||------------------------------------------------------------------------------------------------||
	//|| Optional
	//||------------------------------------------------------------------------------------------------||
	UseSSL    bool `json:"use_ssl,omitempty"`
	PathStyle bool `json:"path_style,omitempty"`
}
