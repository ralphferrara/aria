package aria

//||------------------------------------------------------------------------------------------------||
//|| Import
//||------------------------------------------------------------------------------------------------||

import (
	"aria/app"
	"aria/log"
	"os"
)

//||------------------------------------------------------------------------------------------------||
//|| Main
//||------------------------------------------------------------------------------------------------||

func main() {
	if err := app.Init("config.json"); err != nil {
		log.Error("main", "Startup failed: %v", err)
		os.Exit(1)
	}
	log.Info("main", "Aria started!")
}
