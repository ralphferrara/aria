package encrypt

//||------------------------------------------------------------------------------------------------||
//|| Import
//||------------------------------------------------------------------------------------------------||

import (
	"crypto/sha256"
	"encoding/base64"
)

//||------------------------------------------------------------------------------------------------||
//|| Generate a Random Key
//||------------------------------------------------------------------------------------------------||

func KeyHash(key, str string) string {

	//||------------------------------------------------------------------------------------------------||
	//|| Create the Hash
	//||------------------------------------------------------------------------------------------------||

	keyHash := sha256.Sum256([]byte(key + str))

	//||------------------------------------------------------------------------------------------------||
	//|| Return
	//||------------------------------------------------------------------------------------------------||

	return base64.StdEncoding.EncodeToString(keyHash[:])
}
