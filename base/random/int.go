package random

//||------------------------------------------------------------------------------------------------||
//|| Import
//||------------------------------------------------------------------------------------------------||

import (
	"crypto/rand"
	"math/big"
)

//||------------------------------------------------------------------------------------------------||
//|| Generate a Secure Random Number (up to l digits)
//||------------------------------------------------------------------------------------------------||

func RandomNumber(l int) int {
	if l <= 0 {
		return 0
	}

	max := new(big.Int).Exp(big.NewInt(10), big.NewInt(int64(l)), nil) // 10^l
	n, err := rand.Int(rand.Reader, max)
	if err != nil {
		return 0
	}

	return int(n.Int64())
}
